int			max(int *tab, unsigned int len)
{
	int max;
	size_t i;

	if (!len)
		return (0);
	max =  0;
	i = 0;
	while (i < len)
	{
		if (max < tab[i])
			max = tab[i];
		i++;
	}
	return (max);
}
